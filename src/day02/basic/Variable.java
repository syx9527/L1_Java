package day02.basic;

/**
 * 变量
 *
 * @author SYX
 */
public class Variable {
    public static void main(String[] args) {
/*
        Java是强数据类型
        数据类型 变量名 = 初始化值;
        必须先声明，再使用
*/

        int a = 12;
        int b;
        b = 1;
        System.out.println(a);
        System.out.println(b);

        //    支持多个变量同时定义，但是必须是同一类型
        int c = 5, d = 6, e = 9;


    }
}
